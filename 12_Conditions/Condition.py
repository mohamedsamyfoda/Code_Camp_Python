num1=float(input("please enter num1"))
operator= input("please enter operator")
num2=float(input("please enter num2"))
if operator == "+":
    print(num1+num2)
elif operator == "-":
    print(num1-num2)
elif operator == "*":
    print(num1*num2)
elif operator == "/":
    print(num1/num2)
else:
    print("no resultes")
